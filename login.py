import hashlib
import os

# Dictionary to store user credentials
users = {}

def hash_password(password, salt):
    return hashlib.sha256(salt.encode() + password.encode()).hexdigest()

def sign_up():
    username = input("Choose a username: ")
    if username in users:
        print("‚ùå Username already exists. Try another.")
        return
    password = input("Create a password: ")
    salt = os.urandom(16).hex()  # Generate a random salt
    hashed_password = hash_password(password, salt)
    users[username] = {'salt': salt, 'password': hashed_password}
    print("‚úÖ Account created successfully!\n")

def log_in():
    username = input("Enter your username: ")
    if username not in users:
        print("‚ùå Username not found. Sign up first!\n")
        return
    password = input("Enter your password: ")
    stored_salt = users[username]['salt']
    stored_hashed_password = users[username]['password']
    if hash_password(password, stored_salt) == stored_hashed_password:
        print("‚úÖ Login successful!\n")
    else:
        print("‚ùå Incorrect password. Try again.\n")

def main():
    while True:
        print("\n1Ô∏è‚É£ Sign Up  |  2Ô∏è‚É£ Log In  |  3Ô∏è‚É£ Exit")
        choice = input("Choose an option (1/2/3): ")
        if choice == '1':
            sign_up()
        elif choice == '2':
            log_in()
        elif choice == '3':
            print("Goodbye! üëã")
            break
        else:
            print("‚ùå Invalid choice. Please enter 1, 2, or 3.\n")

if __name__ == "__main__":
    main()

